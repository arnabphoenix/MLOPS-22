from flask import Flask, request, jsonify

from joblib import load


app = Flask(__name__)
svm_clf = load("svm_gamma=0.001_C=0.7.joblib")

@app.route("/", methods = ["GET"])
def hello_world():
    return "<p>Hello, World!</p>"


@app.route("/sm", methods = ["POST"])
def sum():
    print(request.json)
    mx = request.json["mx"]
    nx = request.json["nx"]
    sx = mx + nx
    return str(sx)


@app.route("/pt", methods = ['POST'])
def predict():
    imgarn = request.json['imgarn']

    clsarn = svm_clf.predict([imgarn])

    # print(cls1)

    return str(clsarn[0])


@app.route("/imgcmarn", methods = ['POST'])
def comp_img():
    imgarn1 = request.json['imgarn1']
    imgarn2 = request.json['imgarn2']

    clarn1 = svm_clf.predict([imgarn1])
    clarn2 = svm_clf.predict([imgarn2])

    fgarn = None
    if clarn1[0] == clarn2[0]:
        fgarn = "same"
    else:
        fgarn = "different"


    return str(fgarn)


